let dark = "rgb(51 51 51)";
let yellow = "rgb(240 133 51)";

function linkedin_effect(){
    let linkedin = document.getElementById("linkedin")
    let linkedin_i = document.getElementById("linkedin_i")
    
    linkedin.style.backgroundColor=yellow;
    linkedin_i.style.backgroundColor=yellow;

}

function linkedin_normal(){
    let linkedin = document.getElementById("linkedin")
    let linkedin_i = document.getElementById("linkedin_i")
    
    linkedin.style.backgroundColor=dark;
    linkedin_i.style.backgroundColor=dark;

}


